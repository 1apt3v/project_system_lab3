namespace SolveCalculate.Lib;

public class Calculate
{
    public int CalculateSumma(int num1, int num2)
    {
        return num1 + num2;
    }
    public int CalculateDifference(int num1, int num2)
    {
        return num1 - num2;
    }
    public int CalculateMultiplication(int num1, int num2)
    {
        return num1 * num2;
    }
    public double CalculateQuotient(double num1, double num2)
    {
        return num1 / num2;
    }
}
